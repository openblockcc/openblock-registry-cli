/**
 * @fileoverview
 * PR body templates for openblock-registry submissions.
 *
 * The PR body is generated on the developer's machine and is untrusted (§5.1).
 * Every fact a reviewer needs already comes from trusted sources: the registry
 * bot's authoritative display report (rendered from the published git tag) and
 * the PR's own registry.json / approved/{id}.json diff. So the body carries no
 * details of its own — restating them would only compete for the reviewer's
 * attention with the trustworthy report. It is a one-line pointer, nothing more.
 */

/**
 * Generate PR body for publishing a new plugin or version. Intentionally minimal:
 * it states where the review information actually lives and nothing else.
 * @param {object} options - PR options
 * @param {string} options.pluginId - Plugin ID
 * @returns {string} PR body markdown
 */
const generatePublishPRBody = options => {
    const {pluginId} = options;

    return `Review the registry bot's **authoritative display report** below and the \`registry.json\` / \`approved/${pluginId}.json\` diff. This body is auto-generated by [openblock-registry-cli](https://github.com/openblockcc/openblock-registry-cli) and carries no review information of its own.
`;
};

/**
 * Generate PR title
 * @param {string} action - 'publish'
 * @param {string} pluginId - Plugin ID
 * @param {string} version - Version
 * @returns {string} PR title
 */
const generatePRTitle = (action, pluginId, version) => {
    if (action === 'publish') {
        return `[Publish] ${pluginId}@${version}`;
    }
    return `[Update] ${pluginId}`;
};

module.exports = {
    generatePublishPRBody,
    generatePRTitle
};
